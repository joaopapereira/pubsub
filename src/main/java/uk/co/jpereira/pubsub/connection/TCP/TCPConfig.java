package uk.co.jpereira.pubsub.connection.TCP;

public class TCPConfig {
	private String url;
	private int port;
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	
}
